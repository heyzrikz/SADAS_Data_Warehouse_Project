-- Dati lavorativi singolo dipendente

SELECT DISTINCT CODICE_FISCALE,NOME,COGNOME,EMAIL,DIPARTIMENTO,MANSIONE,DATA_ASSUNZIONE,MAX(LIVELLO) AS LIVELLO_ATTUALE
FROM ANAGRAFICA AS A JOIN CEDOLINI AS C ON A.CODICE_FISCALE = C.CODICE_FISCALE
WHERE CODICE_FISCALE = '0' --sostituire 0 con codice fiscale da ricercare
GROUP BY CODICE_FISCALE,NOME,COGNOME,EMAIL,DIPARTIMENTO,MANSIONE,DATA_ASSUNZIONE