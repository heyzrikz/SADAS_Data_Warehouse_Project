--Dipendenti che hanno stipendio molto più alto o molto più basso rispetto alla media con pari livello.

SELECT CODICE_FISCALE,LIVELLO,STIPENDIO FROM(
SELECT CODICE_FISCALE,MAX(LIVELLO) AS LIVELLO,MAX(STIPENDIO)AS STIPENDIO FROM CEDOLINI_SADAS_2023 
GROUP BY CODICE_FISCALE
)
WHERE LIVELLO = 1 AND (ABS(STIPENDIO - (
SELECT AVG(STIPENDIO) FROM (SELECT CODICE_FISCALE,MAX(LIVELLO) AS LIVELLO,MAX(STIPENDIO)AS STIPENDIO FROM CEDOLINI_SADAS_2023 
GROUP BY CODICE_FISCALE)
)) > 2500)