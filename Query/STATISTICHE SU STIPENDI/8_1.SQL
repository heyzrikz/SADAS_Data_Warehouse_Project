-- Somma di tutti gli stipendi mensili elargiti negli ultimi 12 mesi

SELECT SUM(STIPENDIO) AS STIPENDI_TOTALI FROM (SELECT
	 MESE,
	 ANNO,
	 STIPENDIO,DATEPART('YY',ADD_MONTHS(TODAY,-12))AS A, DATEPART('MM',ADD_MONTHS(TODAY,-12))AS M

FROM
	 CEDOLINI WHERE (ANNO = A AND MESE >= M) OR (ANNO = A + 1 AND MESE < M))