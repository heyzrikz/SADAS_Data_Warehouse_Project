-- Grafico Andamentale stipendio(anche netto e competenze) su 2 anni

SELECT
	*
FROM
	 ( SELECT
	 CODICE_FISCALE, STIPENDIO, STIPENDIO_NETTO, COMPETENZE, ANNO, MESE
FROM
	 CEDOLINI_SADAS_2021 
UNION  ALL SELECT
	 CODICE_FISCALE, STIPENDIO, STIPENDIO_NETTO, COMPETENZE, ANNO, MESE
FROM
	 CEDOLINI_SADAS_2022 )
WHERE
	 CODICE_FISCALE= '0'
ORDER BY
	 ANNO,
	 MESE