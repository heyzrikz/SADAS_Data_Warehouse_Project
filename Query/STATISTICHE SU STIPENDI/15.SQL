--Top N non riceve aumenti da più tempo
SELECT CODICE_FISCALE,MESI_SENZA_AUMENTO,STIPENDIO FROM (
SELECT CODICE_FISCALE , COUNT(STIPENDIO) AS MESI_SENZA_AUMENTO,STIPENDIO, MAX(ANNO) AS ANNO_AUMENTO FROM(
SELECT CODICE_FISCALE,STIPENDIO,ANNO,MESE FROM CEDOLINI 
ORDER BY CODICE_FISCALE,ANNO,MESE
)
GROUP BY CODICE_FISCALE,STIPENDIO
)
WHERE ANNO_AUMENTO = 2023
ORDER BY MESI_SENZA_AUMENTO DESC
LIMIT 10