--Top N dipendenti con lo stipendio attuale più alto
select CODICE_FISCALE,DIPARTIMENTO,FILIALE,STIPENDIO FROM(
select CODICE_FISCALE,MAX(MESE) AS MESE,STIPENDIO,DIPARTIMENTO,FILIALE from cedolini_filiale_dipartimento where anno = 2023 group by CODICE_FISCALE,STIPENDIO,DIPARTIMENTO,FILIALE --SOSTITUISCI 2023 CON L'ANNO DA ANALIZZARE
) ORDER BY STIPENDIO DESC
LIMIT 10